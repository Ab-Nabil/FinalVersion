package com.example.afinal;

public class User {
    int userID;
    String userName;
    String userEmail;
    String userPassword;

    String userWeight;
    String userHeight;
    String userBirthDate;

    String userGender;
    String userFitLevel;
    String userActivityLevel;

    public User() {

    }

    public String getUserGender() {
        return userGender;
    }

    public void setUserGender(String userGender) {
        this.userGender = userGender;
    }

    public String getUserFitLevel() {
        return userFitLevel;
    }

    public void setUserFitLevel(String userFitLevel) {
        this.userFitLevel = userFitLevel;
    }

    public String getUserActivityLevel() {
        return userActivityLevel;
    }

    public void setUserActivityLevel(String userActivityLevel) {
        this.userActivityLevel = userActivityLevel;
    }

    public String getUserWeight() {
        return userWeight;
    }

    public void setUserWeight(String userWeight) {
        this.userWeight = userWeight;
    }

    public String getUserHeight() {
        return userHeight;
    }

    public void setUserHeight(String userHeight) {
        this.userHeight = userHeight;
    }

    public String getUserBirthDate() {
        return userBirthDate;
    }

    public void setUserBirthDate(String userBirthDate) {
        this.userBirthDate = userBirthDate;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }
}
